public class Pesanan20 {
    int kodePesanan20;
    String namaPesanan20;
    int jumlahPesanan20;
    int Harga20, total20;

    public Pesanan20(int a20, String b20, int d20, int e20) {
        this.kodePesanan20 = a20;
        this.namaPesanan20 = b20;
        this.jumlahPesanan20 = d20;
        this.Harga20 = e20;
        this.total20 = d20 * e20;
    }
}
